package mil.br.pamerj.projects.telefonia.dto;

import java.util.Date;

public class LigacaoDTO {
	private Long id;
	private String numero;
	private Date dataHora;
	private Date duracao;
	private double custo;
	private String local;
	private String tipo;
	
	private RamalDTO ramal;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public Date getDataHora() {
		return dataHora;
	}

	public void setDataHora(Date dataHora) {
		this.dataHora = dataHora;
	}

	public Date getDuracao() {
		return duracao;
	}

	public void setDuracao(Date duracao) {
		this.duracao = duracao;
	}

	public double getCusto() {
		return custo;
	}

	public void setCusto(double custo) {
		this.custo = custo;
	}

	public String getLocal() {
		return local;
	}

	public void setLocal(String local) {
		this.local = local;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	
	public RamalDTO getRamal() {
		return ramal;
	}

	public void setRamal(RamalDTO ramal) {
		this.ramal = ramal;
	}

    @Override
    public String toString() {
        return "LigacaoDTO{" + "id=" + id + ", numero=" + numero + ", dataHora=" + dataHora + ", duracao=" + duracao + ", custo=" + custo + ", local=" + local + ", tipo=" + tipo + ", ramal=" + ramal.getNumero() + '}';
    }

}
